CREATE TABLE IF NOT EXISTS brand_info (
    id SERIAL PRIMARY KEY,
    name VARCHAR(50) NOT NULL
    );

CREATE TABLE IF NOT EXISTS products_active (
    id SERIAL PRIMARY KEY,
    brand_id INT NOT NULL,
    model VARCHAR(100) NOT NULL,
    os VARCHAR(100),
    FOREIGN KEY (brand_id) REFERENCES brand_info (id) ON DELETE CASCADE
);

CREATE TABLE IF NOT EXISTS sales_info (
      id SERIAL PRIMARY KEY,
      product_id INT NOT NULL,
      sold_units INT NOT NULL,
      FOREIGN KEY (product_id) REFERENCES products_active (id) ON DELETE CASCADE
);



